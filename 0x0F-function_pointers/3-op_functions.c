#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - Function that gets the sum of a and b
 * @a: integer
 * @b: integer
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function to get the subtraction
 * @a: integer
 * @b: integer
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function for multiplication
 * @a: integer
 * @b: integer
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function for division
 * @a: integer
 * @b: integer
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Function for modulus
 * @a: integer
 * @b: integer
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
